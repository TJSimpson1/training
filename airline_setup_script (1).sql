DROP TABLE customer CASCADE CONSTRAINTS;
DROP TABLE passengers CASCADE CONSTRAINTS;
DROP TABLE flights CASCADE CONSTRAINTS;
DROP TABLE flight_dates CASCADE CONSTRAINTS;
DROP TABLE bookings CASCADE CONSTRAINTS;
DROP TABLE passengers_bookings CASCADE CONSTRAINTS;

CREATE TABLE customer
(
customer_id NUMBER(9) PRIMARY KEY,
name VARCHAR2(50),
email VARCHAR2(50),
phone VARCHAR2(15),
country VARCHAR2(50)
);

CREATE TABLE passengers
(
passenger_id NUMBER(10) PRIMARY KEY,
name VARCHAR2(50),
age NUMBER(3),
passport_number NUMBER(12),
passport_expiry DATE,
nationality VARCHAR(2)
);

CREATE TABLE flights
(
flight_code VARCHAR2(7) PRIMARY KEY,
origin_airport VARCHAR2(3),
destination_airport VARCHAR2(3),
number_of_seats NUMBER(3)
);

CREATE TABLE flight_dates
(
flight_date_id NUMBER(15) PRIMARY KEY,
flight_code VARCHAR2(7),
scheduled_departure_date DATE,
scheduled_arrival_date DATE,
actual_departure_date DATE,
actual_arrival_date DATE,
current_price NUMBER(8,2),
seats_currently_booked NUMBER(3),
FOREIGN KEY (flight_code) REFERENCES flights(flight_code)
);

CREATE TABLE bookings
(
booking_id VARCHAR2(7) PRIMARY KEY,
customer_id NUMBER(9),
flight_date_id NUMBER(15),
total_price NUMBER(8,2),
FOREIGN KEY (customer_id) REFERENCES customer(customer_id),
FOREIGN KEY (flight_date_id) REFERENCES flight_dates(flight_date_id)
);

CREATE TABLE passengers_bookings
(
booking_id VARCHAR2(7),
passenger_id NUMBER(10),
FOREIGN KEY (booking_id) REFERENCES bookings(booking_id),
FOREIGN KEY (passenger_id) REFERENCES passengers(passenger_id),
PRIMARY KEY (booking_id, passenger_id)
);



